//vue的常用指令(将新建的vue对象以各种方式绑定在HTML标签上,从而实现不同的动态效果):

//v-bind 为HTML标签绑定属性值,如设置href,css样式等
//v-model 在表单元素上创建双向数据绑定
    //**一旦在标签上绑定了某个属性,那么一定要在vue数据模型中声明这个属性,否则会报错**

//v-on 绑定事件监听器
    //**有了v-on就不需要绑定事件监听了,直接在标签上绑定事件即可**

//v-if/v-else-if/v-else 根据表达式的值的真假条件渲染元素
//v-show 根据表达式的值的真假条件展示元素
//v-if等是根据条件判断来决定是否!!渲染!!元素,而v-show是根据条件判断来决定是否!!显示!!元素

//v-for 列表渲染,遍历容器的元素或对象的属性


